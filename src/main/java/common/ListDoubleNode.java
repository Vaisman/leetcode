package common;

public class ListDoubleNode {
    public int val;
    public ListDoubleNode left;
    public ListDoubleNode right;

    public ListDoubleNode() {
    }

    public ListDoubleNode(int _val, ListDoubleNode _left, ListDoubleNode _right) {
        val = _val;
        left = _left;
        right = _right;
    }
}
